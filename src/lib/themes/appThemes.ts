export function isAppThemeId(value: string | null): value is AppThemeId {
  return value !== null && APP_THEME_IDS.includes(value as AppThemeId);
}

export function applyAppTheme(themeId: AppThemeId, variant: AppThemeVariant) {
  if (typeof document === 'undefined') return;
  const root = document.documentElement;
  const tokens = appThemes[themeId][variant];
  root.dataset.colorTheme = themeId;
  for (const [property, value] of Object.entries(tokens)) {
    root.style.setProperty(property, value);
  }
}
